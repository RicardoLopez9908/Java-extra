package juego;

public class POOparametros {

//atributos del objeto
int suma;
int resta;
int multiplicacion;
int division;

//metodos con parametros:

public void sumarArgumentos(int a, int b){
	suma= a+b;
}
public void restarArgumentos(int a, int b){
	resta= a-b;
}	
public void multiplicarArgumentos(int a, int b){
	multiplicacion= a*b;
}
public void dividirArgumentos(int a,int b){
	division= a/b;
}
	

}

